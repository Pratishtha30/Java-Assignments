/*
Write a program that displays the area and perimeter of a rectangle with the width of
4.5 and height of 7.9 using the following formula:
area = width x height
perimeter = 2 x (height + width)
*/

public class Exercise01_9{
 public static void main(String[] args){

  // Display area
  System.out .println("Area =  " + (4.5 * 7.9));
  // Display perimeter
  System.out .println("Perimeter = " + (2 * (4.5 + 7.9)));
 }
}
   

